// hone.proto — Connect-RPC contract for the `hone` bounded context.
//
// Hone is the desktop focus cockpit (Electron, macOS-first) in the druz9
// ecosystem. Backend responsibility is narrow:
//
//   - Daily Plan  — AI-synthesised list of what the user should hone today,
//                   seeded from Skill Atlas gaps + calendar + recent GitHub.
//   - Focus       — pomodoro session lifecycle + streak/stat aggregation.
//   - Notes       — private markdown store with AI-generated connections
//                   (embeddings via bge-small, cosine over note corpus +
//                   cross-domain artefacts like PRs / tasks / sessions).
//   - Whiteboard  — persisted tldraw state + on-demand AI critique
//                   (reuses TaskSysDesignCritique from llmchain).
//
// What hone is NOT: task authoring, arena, mock interviews, rating. Those
// live on druz9.ru and are deep-linked from the client via druz9://… URIs.
